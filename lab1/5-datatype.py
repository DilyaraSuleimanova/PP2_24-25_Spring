# STR
x = "Hello World"
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# INT
x = 20
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# FLOAT
x = 20.5
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# COMPLEX
x = 1j
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# LIST
x = ["apple", "banana", "cherry"]
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# TUPLE
x = ("apple", "banana", "cherry")
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# RANGE
x = range(6)
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# DICT
x = {"name" : "John", "age" : 36}
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# SET
x = {"apple", "banana", "cherry"}
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# FROZENSET
x = frozenset({"apple", "banana", "cherry"})
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# BOOL
x = True
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# BYTES
x = b"Hello"
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# BYTEARRAY
x = bytearray(5)
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# MEMORYVIEW
x = memoryview(bytes(5))
print("x: ", x)
print("the data type of x: ", type(x)) 
print()

# NONETYPE
x = None
print("x: ", x)
print("the data type of x: ", type(x)) 
print()